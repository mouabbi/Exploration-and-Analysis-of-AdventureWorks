SELECT 
    SalesOrderID, 
    RevisionNumber, 
    OrderDate, 
    DueDate, 
    ShipDate, 
    Status, 
    OnlineOrderFlag, 
    SalesOrderNumber, 
    PurchaseOrderNumber, 
    AccountNumber, 
    CustomerID, 
    SalesPersonID, 
    TerritoryID, 
    BillToAddressID, 
    ShipToAddressID, 
    ShipMethodID, 
    CreditCardID, 
    CreditCardApprovalCode, 
    CurrencyRateID, 
    SubTotal, 
    TaxAmt, 
    Freight, 
    TotalDue, 
    Comment, 
    rowguid, 
    ModifiedDate,
    COUNT(*) AS DuplicateCount
FROM [AdventureWorks2019].[Sales].[SalesOrderHeader]
GROUP BY 
    SalesOrderID, 
    RevisionNumber, 
    OrderDate, 
    DueDate, 
    ShipDate, 
    Status, 
    OnlineOrderFlag, 
    SalesOrderNumber, 
    PurchaseOrderNumber, 
    AccountNumber, 
    CustomerID, 
    SalesPersonID, 
    TerritoryID, 
    BillToAddressID, 
    ShipToAddressID, 
    ShipMethodID, 
    CreditCardID, 
    CreditCardApprovalCode, 
    CurrencyRateID, 
    SubTotal, 
    TaxAmt, 
    Freight, 
    TotalDue, 
    Comment, 
    rowguid, 
    ModifiedDate
HAVING COUNT(*) > 1;
